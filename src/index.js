const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    const symbolMap = {
        '10': '.',
        '11': '-'
    };

    return expr.match(/.{1,10}/g).map((str) => {
        if (str === '**********') return ' ';

        const morseCode = str.replace(/^0+/, '')
            .match(/.{1,2}/g)
            .map((symbol) => symbolMap[symbol])
            .join('');

        return MORSE_TABLE[morseCode];
    }).join('');
}

module.exports = {
    decode
}